const user = {
    firstName: "Gunawan",
    lastName: "Aji",
    age: 21
}
/*
Sebelum ES6, cara lama dalam menggabungkan nilai string adalah dengan menggunakan operator (+). 
*/
console.log("Nama: " + user.firstName + " " + user.lastName + ", Umur: " + user.age);

/* output
Nama: Gunawan Aji, Umur: 21
*/